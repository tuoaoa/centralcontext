#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const assert = require('assert');

const repoRoot = path.resolve(__dirname, '..');
const {
  redactAll,
  parsePriorityProject,
  parseActiveTask,
  parseRecentDecisions,
  parseRecentWorkLog,
  parseBlockers,
  parseNextSteps
} = require('./generate-daily-digest');

const dailyDigestPath = path.join(repoRoot, 'context/DAILY_DIGEST.md');

// Run the generator to produce the output
const { execSync } = require('child_process');

function runGenerator() {
  execSync('node scripts/generate-daily-digest.js', { stdio: 'inherit' });
}

function test() {
  console.log('\n\x1b[36m==================================================\x1b[0m');
  console.log('\x1b[36m🧪 TESTING DAILY DIGEST WORKFLOW (v2.1)\x1b[0m');
  console.log('\x1b[36m==================================================\x1b[0m\n');

  // 1. Run generator and verify generation
  console.log('Running daily digest generator...');
  runGenerator();
  
  assert(fs.existsSync(dailyDigestPath), 'DAILY_DIGEST.md must exist after generator run');
  console.log('  - Digest file generated successfully (PASSED)');

  const content = fs.readFileSync(dailyDigestPath, 'utf8');

  // 2. Verify all expected required headings are present
  const requiredHeadings = [
    '# Daily Digest',
    '## Current Focus',
    '## Recent Decisions',
    '## Recent Work',
    '## Memory Activity',
    '## Open Blockers',
    '## Recommended Next Actions'
  ];

  for (const heading of requiredHeadings) {
    assert(content.includes(heading), `Daily Digest must contain section: "${heading}"`);
  }
  console.log('  - All structural headings present (PASSED)');

  // 3. Verify priority source resolution
  assert(content.includes('**Active Project**: `qlythuexe`'), 'Resolved focus project must be qlythuexe from CURRENT_STATE.md');
  assert(!content.includes('**Active Project**: `SaveX`'), 'Old state project (SaveX) must not override current state focus');
  assert(!content.includes('**Active Project**: `GiveGet`'), 'Archive state project (GiveGet) must not override current state focus');
  console.log('  - Priority source resolution matches SOURCE_PRIORITY.md (PASSED)');

  // Verify safety / redaction of secrets and internal test markers directly on a mock string
  const mockRaw = 'This is an API key: sk-or-v1-abcdef123, founder: FOUNDER_CODE_1234, test marker: SECRET_CONTEXT_TEST_9999, auto pass: AUTO_CONTEXT_PASS, validation: CentralContext verification test run 3';
  const mockRedacted = redactAll(mockRaw);
  
  assert(!mockRedacted.includes('sk-or-v1-abcdef123'), 'API key must be redacted');
  assert(mockRedacted.includes('[REDACTED_OPENROUTER_KEY]'), 'OpenRouter key placeholder present');
  assert(!mockRedacted.includes('FOUNDER_CODE_1234'), 'Founder code must be redacted');
  assert(mockRedacted.includes('[REDACTED_FOUNDER_CODE]'), 'Founder code placeholder present');
  assert(!mockRedacted.includes('SECRET_CONTEXT_TEST_9999'), 'Test marker must be redacted');
  assert(!mockRedacted.includes('AUTO_CONTEXT_PASS'), 'Auto pass must be redacted');
  assert(!mockRedacted.includes('CentralContext verification test run 3'), 'Validation string must be redacted');
  assert(mockRedacted.includes('[REDACTED_TEST_MARKER]'), 'Test marker placeholder present');

  // Verify the generated content has no leaks
  assert(!content.includes('FOUNDER_CODE_8827'), 'Founder validation code must not be exposed');
  assert(!content.includes('SECRET_CONTEXT_TEST_7791'), 'SECRET_CONTEXT_TEST_7791 must not leak');
  assert(!content.includes('SECRET_CONTEXT_TEST_8892'), 'SECRET_CONTEXT_TEST_8892 must not leak');
  assert(!content.includes('AUTO_CONTEXT_PASS'), 'AUTO_CONTEXT_PASS validation code must not leak');
  assert(!content.includes('CentralContext verification test run 3'), 'Validation check strings must not leak');
  console.log('  - Redaction of tokens and markers verified (PASSED)');

  // 5. Test determinism on multiple runs
  const firstRunContent = fs.readFileSync(dailyDigestPath, 'utf8');
  execSync('node scripts/generate-daily-digest.js', { stdio: 'ignore' });
  const secondRunContent = fs.readFileSync(dailyDigestPath, 'utf8');

  assert.strictEqual(firstRunContent, secondRunContent, 'Re-running generator must be 100% byte-identical');
  console.log('  - Byte-identical outputs across sequential runs (PASSED)');

  // Confirm NO "Generated on:" date or dynamic ISO time stamp is generated
  assert(!content.includes('Generated on:'), 'Markdown must not contain Generated on date headings');
  assert(content.includes('Generated by**: CentralContext Daily Digest Generator'), 'Markdown must use stable deterministic generator header');
  
  const dynamicDateRegex = /generated\s+on\s*:\s*\d{4}-\d{2}-\d{2}/gi;
  assert(!dynamicDateRegex.test(content), 'No dynamic generated on timestamp should be present in the markdown');
  console.log('  - No calendar dates or dynamic timestamps created (PASSED)');

  // 6. Recommendation count <= 3
  const nextStepsList = parseNextSteps(fs.readFileSync(path.join(repoRoot, 'context/CURRENT_STATE.md'), 'utf8'));
  assert(nextStepsList.length <= 3, 'Recommended next actions must not exceed 3 items');
  console.log(`  - Recommended next actions count is: ${nextStepsList.length} (<= 3) (PASSED)`);

  // 7. Missing DB handled gracefully without crashing
  console.log('Testing missing database fallback safety...');
  const dbPath = path.join(repoRoot, 'data/centralcontext.db');
  const tempDbPath = path.join(repoRoot, 'data/centralcontext.db.temp-test-digest');
  
  let dbRenamed = false;
  try {
    if (fs.existsSync(dbPath)) {
      fs.renameSync(dbPath, tempDbPath);
      dbRenamed = true;
    }

    // Run generator with database completely offline
    execSync('node scripts/generate-daily-digest.js', { stdio: 'ignore' });
    const offlineContent = fs.readFileSync(dailyDigestPath, 'utf8');
    
    assert(offlineContent.includes('Memory database is currently offline or unavailable.') || offlineContent.includes('Could not load memory activity'), 'Offline database state must be handled with warning');
    console.log('  - Offline database handled gracefully (PASSED)');

  } finally {
    if (dbRenamed && fs.existsSync(tempDbPath)) {
      fs.renameSync(tempDbPath, dbPath);
    }
  }

  console.log('\n\x1b[32m==================================================\x1b[0m');
  console.log('\x1b[32m🎉 ALL DAILY DIGEST WORKFLOW TESTS PASSED!\x1b[0m');
  console.log('\x1b[32m==================================================\x1b[0m\n');
}

test();
